package primitives_and_objects;

import java.util.Scanner;

public class Main {
    public static void main(String[] args) {


        int a = 10 + 2;
        int b = 10 - 2;
        int c = 10 * 2;
        int d = 10 / 2;
        int e = 10 % 3;

        System.out.println("a = " + a);
        System.out.println("b = " + b);
        System.out.println("c = " + c);
        System.out.println("d = " + d);
        System.out.println("e = " + e);


    }
}
